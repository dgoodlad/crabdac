// # Data formats:
//
// ## USB
//
// 24-bit PCM in 32-bit slots, MSB-aligned
//
//      0x12345600
//
// ## STM32
//
// ### I2S Philips standard
//
//     CK _-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-
//            |                                               |
//     WS --________________________________________________________________--------
//            |<----------------- 24-bit data --------------->|
//     SD ~~~~M.............................................L.0 0 0 0 0 0 0 0 M.....
//            S                                             S                 S
//            B                                             B                 B
//
//     0x1234, 0x56XX
//
// ### MSB justified
//
//     CK _-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-
//          |                                               |               |
//     WS --________________________________________________________________--------
//          |<----------------- 24-bit data --------------->|<- 0 forced  ->|
//     SD ~~M.............................................L.0 0 0 0 0 0 0 0 M.....
//          S                                             S                 S
//          B                                             B                 B
//
//     0x1234, 0x56XX
//
// ### LSB justified
//
// Not worth including, unsupported by PCM179x
//
// ### PCM standard
//
// Not worth including, unsupported by PCM179x
//
// ## PCM1794a
//
// I2S Philips!
//
// ## UDA1334 (Adafruit headphone amp)
//
// I2S by default
